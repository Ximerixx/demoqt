PRAGMA foreign_keys = ON;

-- Схема (совпадает с приложением), если файл БД пустой или таблиц ещё нет
CREATE TABLE IF NOT EXISTS employees (
  id INTEGER PRIMARY KEY AUTOINCREMENT,
  full_name TEXT NOT NULL,
  position TEXT NOT NULL,
  department TEXT NOT NULL
);
CREATE TABLE IF NOT EXISTS components (
  id TEXT PRIMARY KEY,
  name_gost TEXT NOT NULL,
  alternative_names TEXT,
  type TEXT NOT NULL,
  characteristics TEXT
);
CREATE TABLE IF NOT EXISTS clients (
  id TEXT PRIMARY KEY,
  phone TEXT,
  first_name TEXT NOT NULL,
  last_name TEXT NOT NULL,
  discount_percent REAL
);
CREATE TABLE IF NOT EXISTS sales (
  id INTEGER PRIMARY KEY AUTOINCREMENT,
  client_id TEXT NOT NULL REFERENCES clients(id),
  employee_id INTEGER NOT NULL REFERENCES employees(id),
  sale_datetime TEXT NOT NULL
);
CREATE TABLE IF NOT EXISTS sale_items (
  id INTEGER PRIMARY KEY AUTOINCREMENT,
  sale_id INTEGER NOT NULL REFERENCES sales(id),
  component_id TEXT NOT NULL REFERENCES components(id),
  quantity INTEGER NOT NULL,
  unit_price REAL
);

-- Представление: продажи с составом (кто купил, кто продал, какие компоненты, количество, цена)
CREATE VIEW IF NOT EXISTS v_sales_detail AS
SELECT
  s.id AS sale_id,
  s.sale_datetime,
  s.client_id,
  (c.first_name || ' ' || c.last_name) AS client_name,
  s.employee_id,
  e.full_name AS employee_name,
  si.component_id,
  comp.name_gost AS component_name,
  si.quantity,
  si.unit_price,
  (si.quantity * COALESCE(si.unit_price, 0)) AS line_total
FROM sales s
JOIN sale_items si ON s.id = si.sale_id
JOIN components comp ON si.component_id = comp.id
JOIN clients c ON s.client_id = c.id
JOIN employees e ON s.employee_id = e.id;

